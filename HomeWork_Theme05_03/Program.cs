        /// Метод возвращает рандомное чичсло (диапазон 0-10)
        /// </summary>
        /// <returns></returns>
        static int CreateNumber()
        {
            Random r = new Random();

            int N = r.Next(10);
            Console.WriteLine($"Число: {N}");
            return N;
        }

        /// <summary>
        /// Метод возвращает двумерный массив (матрицу) 3 на 3 
        /// </summary>
        /// <returns></returns>
        static int[,] CreateMatrix()
        {
            Random r = new Random();

            Console.WriteLine("Матрица: ");

            int[,] M = new int[3, 3];
            for (int i = 0; i < 3; i++)
            {
                for (int j = 0; j < 3; j++)
                {
                    M[i, j] = r.Next(10);
                    Console.Write($"{M[i, j]} ");
                }
                Console.WriteLine();
            }
            return M;
        }

