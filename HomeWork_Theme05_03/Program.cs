            for (int i = 0; i < 3; i++)
            {
                for (int j = 0; j < 3; j++)
                {
                    compMatrix[i, j] = M[i, j] * N;
                    Console.Write($"{compMatrix[i, j]} ");
                }
                Console.WriteLine();
            }
        }

